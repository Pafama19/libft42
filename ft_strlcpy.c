#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	source_len;
	size_t	i;

	i = 0;
	source_len = ft_strlen(src);
	if (dstsize == 0)
		return (source_len);
	while (i < (dstsize - 1) && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (source_len);
}
